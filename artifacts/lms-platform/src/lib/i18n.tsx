interface I18nContextType {
  language: Language;
  /** alias لـ language — للتوافق مع مكونات الـ storefront */
  lang: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nContextType | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem("lms_language") as Language;
    return saved === "en" || saved === "ar" ? saved : "en";
  });

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem("lms_language", lang);
  };

  useEffect(() => {
    document.documentElement.dir = language === "ar" ? "rtl" : "ltr";
    document.documentElement.lang = language;
    const fontLink = document.getElementById("arabic-font");
    if (language === "ar" && !fontLink) {
      const link = document.createElement("link");
      link.id = "arabic-font";
      link.rel = "stylesheet";
      link.href = "https://fonts.googleapis.com/css2?family=Cairo:wght@400;500;600;700&display=swap";
      document.head.appendChild(link);
    }
    if (language === "ar") {
      document.body.style.fontFamily = "'Cairo', sans-serif";
    } else {
      document.body.style.fontFamily = "";
    }
  }, [language]);

  const t = (key: string) => translations[language]?.[key] ?? key;

  return (
    // lang = language عشان الكومبوننتات اللي بتستخدم { lang } تشتغل صح
    <I18nContext.Provider value={{ language, lang: language, setLanguage, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}